from math import radians, sin, cos, tan

angulo = int(input("informe um ângulo:"))
print("cosseno: {:.2f}".format(cos(radians(angulo))))
print("seno: {:.2f}".format(sin(radians(angulo))))
print("tangente: {:.2f}".format(tan(radians(angulo))))

